#include <stdio.h>
/*
* main - this function demonstrates the usage of while loop in code execution.
* dig - refers to digit/number used in demonstration of the while loop. 
*/

int main(){

    // variable definition
    int dig = 1;

    // while loop example
    while(dig <= 6){
        printf("This is the while loop executing\n");
        dig++;

    }
    printf("The loop has ended\n");
    return 0;
}